  getCurrentDateTime() : string
  {
    let now = new Date();
    let month = (now.getMonth() + 1).toString();
    let day = now.getDate().toString();
    if (parseInt(month) < 10)
      month = `0${month}`;
    if (parseInt(day) < 10)
      day = `0${day}`;

    let today = `${now.getFullYear()}-${month}-${day}T00:00`; 
    return today;
  }
