  public returnTimezoneId(selectedDate) : string
  {
    let objdatetime = new Date(selectedDate);
  
    let timezone = objdatetime.toTimeString();
  
    let tzstr = timezone.split("(");
  
    let timezoneid = tzstr[1].toString().replace(")","");
    return timezoneid;
  } 
  OnSearch() {
    
    if(!this.validateBookingDuration())
    {
        this.dialog.open(SharedDialogBoxComponent, {
          data : {
            value : "The minimum duration to book a venue with Visneto is 1 hour."
          }
        });
        this.VenueSearchForm.reset();
        this.VenueSearchForm.markAsUntouched();
        this.VenueSearchForm.markAsPristine();
        return;
    }

    if(!this.validateDate())
    {
          this.dialog.open(SharedDialogBoxComponent, {
            data : {
              value : "Invalid date time selection, please select a valid date to continue booking with Visento!"
            }
          });
          this.VenueSearchForm.reset();
          this.VenueSearchForm.markAsUntouched();
          this.VenueSearchForm.markAsPristine();
          return;
    }
      
    let venueSearch: SearchModel = new SearchModel(this.VenueSearchForm.value);
    this.bookSvc.SetTimeZone = this.returnTimezoneId(this.GetFromDate.value);
    this.bookSvc.SetBookedFromDate = moment(this.GetFromDate.value).toISOString();
    this.bookSvc.SetBookedToDate = moment(this.GetToDate.value).toISOString();
    this.bookSvc.SetTimeZone = this.returnTimezoneId(this.GetFromDate.value);
    let searchData: string = JSON.stringify(venueSearch);
    this.spinOn();
    this.http.post('/api/v1/Venue/search', searchData, this.generateHeaders()).subscribe((data: Venue[]) => {
      
      if (data) 
      {
        this.viewVenue.SetVenues = data;
        this.spinOff();
        if (this.viewVenue.GetVenues.length === 0) {         
          this.dialog.open(SharedDialogBoxComponent, {
            data : {
              value : "No matches found for the requirement given, please re-enter your criteria."
            }
          });
        }
        else {
          this.router.navigate(['/home/search']);
        }

      }
    }, 
    error => {
        this.dialog.open(SharedDialogBoxComponent, {
          data : {
            value : 'Error, please retry the searching later.'
          }
        });
    });

    
  }
